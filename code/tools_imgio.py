#程序说明：图片读取的一些工具函数


import cv2 as cv
import numpy as np
import glob
import os


# 函数说明：
# 根据文件的路径，提取不带文件夹和后缀名字的文件名
# 输入数据：
# img_paths：这个是完整的带文件夹和后缀的文件名列表
# 输出数据：
# base_names：不带后缀，不带文件夹的文件名
#编写人员：刘辉-西安邮电大学
#编写日期：2021.08.06于西安市富力城
def get_base_names(img_paths):

    flag_first = True
    
    for img_path in img_paths:
        if flag_first:            
            split_list = os.path.splitext(os.path.basename(img_path))
            base_names = split_list[-2]
            base_names = np.expand_dims(base_names, axis=0)
            flag_first = False
        else:
            split_list = os.path.splitext(os.path.basename(img_path))
            base_name = split_list[-2]
            base_name = np.expand_dims(base_name, axis=0)
            base_names = np.concatenate((base_names,base_name),axis = 0)
            
    return base_names

#函数说明：根据图片的路径列表，将图片读取出来
#输入：
#img_paths:路径列表，包含的每一个路径要细致到图片的扩展名
#resize：resize的大小，默认128*128
#输出：
#imgs：图片列表，每个列表包含一张三维张量，H W C，C的排列顺序是RGB
#编写人员：刘辉-西安邮电大学
#编写日期：2021.08.06于西安市富力城
def get_imgs_RGB(img_paths,resize = (128,128)):       
    
    flag_first = True

    for img1_path in img_paths:
        if flag_first:
            imgs = cv.imread(img1_path.replace('\\','\\\\'))
            imgs = cv.resize(imgs,resize)
            #将图片通道转成rgb的顺序
            b,g,r = cv.split(imgs)
            imgs = cv.merge([r,g,b])
            imgs = np.expand_dims(imgs, axis=0)            
            flag_first = False

        else:
            img = cv.imread(img1_path.replace('\\','\\\\'))
            img = cv.resize(img,resize)
            #将图片通道转成rgb的顺序
            b,g,r = cv.split(img)
            img = cv.merge([r,g,b])
            img = np.expand_dims(img, axis=0)        
            imgs = np.concatenate((imgs,img),axis = 0)

    return imgs


# 函数说明：
# 根据文件夹的路径，读取文件夹里面的所有图片路径
# 输入数据：
# img_floder_path：图片的文件夹的路径,注意要加上'\\'或者'/'
# 输出数据：
# img_paths：文件夹路径下所有的jpg,tif和png格式的图片路径列表
#编写人员：刘辉-西安邮电大学
#编写日期：2021.08.06于西安市富力城
def get_img_paths(img_floder_path = ""):

    #获取img_floder_path文件夹里所有的jpg,tif和png格式的图片路径
    img_paths=glob.glob(pathname = img_floder_path + '*.jpg') + \
                glob.glob(pathname = img_floder_path + '*.tif') + \
                glob.glob(pathname = img_floder_path + '*.png')   
            
    return img_paths
